/********************************************** RCC_APB1ENR REGISTER ********************************/

#define RCC_APB1ENR 		*((volatile u32*)0x4002101C)

//// INTERFACE //// 

/************************************************************************************************/
/********************************************** RCC_BDCR REGISTER ********************************/

#define RCC_BDCR 			*((volatile u32*)0x40021020)
	
/************************************************************************************************/
/********************************************** RCC_CSR REGISTER ********************************/

#define RCC_CSR  			*((volatile u32*)0x40021024)




#endif
